          <div class="modal-body">
              <p>
                  1.    APRESENTAÇÃO<br>
                  <br>
                  1.1    A Empresa Mineira de Comunicação - EMC – mantém o compromisso de proteger a privacidade e a proteção dos dados pessoais de usuários coletados na execução de seus serviços.<br>
                  1.2    Este documento atende às disposições da Lei Federal 13.709/18 - Lei Geral de Proteção de Dados Pessoais - e aborda as diretrizes para coleta, tratamento, armazenamento e descarte de informações pessoais, de todos os usuários de serviços fornecidos pela EMC<br>
                  <br>
                  2.    PREMISSAS<br>
                  <br>
                  2.1    A EMC poderá publicar regras específicas para cada serviço, subordinadas aos princípios da Lei Geral de Proteção de Dados Pessoais, sem prejuízo das disposições constantes nos contratos celebrados.
                  2.2    Na ausência de regras particulares para um dado serviço, ou em caso de conflito entre regras específicas de um serviço, seus respectivos contratos e o presente documento, prevalecerá esta Diretriz de Privacidade e Proteção de Dados Pessoais.<br>
                  2.3    Ao acessar e/ou utilizar os serviços fornecidos ou operados pela EMC, o usuário declara ter capacidade para aceitação dos termos e condições deste documento de privacidade e proteção de dados pessoais, para todos os fins de direito.<br>
                  2.4    Caso o usuário não se enquadre na descrição do item 2.3, e/ou não concorde com os termos e condições deste documento, ainda que em parte, não deve acessar e/ou utilizar os serviços oferecidos ou operados pela EMC.
                  <br>
                  3.    DIRETRIZES<br>
                  <br>
                  Coleta e Uso de Informações<br>
                  <br>
                  3.1    O Usuário está ciente de que fornece informação de forma consciente e voluntária por meio de formulário, ou dos serviços operados pela EMC<br>
                  3.2    Quando o Usuário realiza cadastro e/ou preenche formulários disponibilizados pela EMC, os dados pessoais solicitados são mantidos sob sigilo e utilizados apenas para o propósito que motivou o cadastro.<br>
                  <br>
                  Compartilhamento e Tratamento de Informações Pessoais<br>
                  <br>
                  3.3    A EMC pode divulgar os dados pessoais coletados para terceiros, nas seguintes situações e nos limites exigidos e autorizados pela Lei Geral de Proteção de Dados Pessoais:<br>
                  a)    Com os seus clientes e parceiros, quando necessário e/ou apropriado à prestação de serviços;<br>
                  b)    Com as empresas e indivíduos contratados para a execução de determinadas atividades e serviços em nome da Empresa Mineira de Comunicação - EMC;<br>
                  c)    Com fornecedores e parceiros para consecução dos serviços contratados com a EMC (como tecnologia da informação, entre outros);<br>
                  d)    Para propósitos administrativos, como pesquisa, planejamento, desenvolvimento de serviços, segurança e gerenciamento de risco;<br>
                  e)    Em decorrência de obrigação legal, determinação de autoridade competente, ou decisão judicial.<br>
                  3.4    A EMC não disponibiliza os dados pessoais coletados nos serviços para corretores de lista de e-mail.<br>
                  3.5    Nas hipóteses de compartilhamento de Dados Pessoais com terceiros, todos os sujeitos mencionados nos itens a) a e) devem utilizar os Dados Pessoais partilhados de maneira consistente e de acordo com os propósitos para os quais foram coletados (ou com os quais o usuário consentiu previamente) e de acordo com o que foi determinado por este documento, por outras declarações de privacidade de websites ou países, e por todas as leis de privacidade e proteção de dados aplicáveis.<br>
                  3.6    A EMC solicita, coleta, processa, armazena ou compartilha, dados pessoais de crianças e adolescentes, nos casos com base legal ou, na sua ausência, com o devido consentimento de seus representantes legais.<br>
                  <br>
                  Divulgação dos Dados<br>
                  <br>
                  3.7    Em certas circunstâncias, a EMC pode divulgar Dados Pessoais, na medida necessária ou apropriada, com os seguintes objetivos:<br>
                  a)    Cumprir com uma legislação que exija tal divulgação;<br>
                  b)    Investigar, impedir ou tomar medidas relacionadas a atividades ilegais suspeitas ou reais ou para cooperar com órgãos públicos ou para proteger a segurança nacional;<br>
                  c)    Execução de seus contratos;<br>
                  d)    Investigar e se defender contra quaisquer reivindicações ou alegações de terceiros;<br>
                  e)    Proteger a segurança ou a integridade dos serviços (por exemplo, o compartilhamento com empresas que estão sofrendo ameaças semelhantes);<br>
                  f)    Exercer ou proteger os direitos, a propriedade e a segurança da EMC e suas empresas coligadas;<br>
                  g)    Proteger os direitos e a segurança pessoal de seus empregados, usuários ou do público;<br>
                  h)    Em caso de venda, compra, fusão, reorganização, liquidação ou dissolução da EMC.<br>
                  <br>
                  Armazenamento de Dados Pessoais<br>
                  <br>
                  3.8    A retenção dos Dados Pessoais é realizada pela EMC nas seguintes situações:<br>
                  a)    Enquanto o cadastro do usuário estiver ativo, até que seja feito o eventual requerimento de exclusão.<br>
                  b)    Para consecução de seus serviços.
                  c)    Para cumprimento de obrigações legais, resolver disputas, manter a segurança, evitar fraudes e abusos.<br>
                  <br>
                  Segurança de Informações Pessoais<br>
                  <br>
                  3.9    Os Dados Pessoais são armazenados nas bases da EMC, de acordo com a legislação de dados vigente.<br>
                  3.10    A EMC e seus fornecedores utilizam vários procedimentos de segurança para proteger a confidencialidade, segurança e integridade dos Dados Pessoais, prevenindo a ocorrência de eventuais danos em virtude do tratamento desses dados.<br>
                  3.11    A Empresa Mineira de Comunicação - EMC utiliza medidas de segurança e monitora seus sistemas para verificar vulnerabilidades a ataques e proteger os Dados Pessoais contra divulgação não autorizada, mau uso ou alteração. A EMC está preparada para agir em caso de incidentes de segurança da informação, responsabilizando-se pelo tratamento dos incidentes.<br>
                  <br>
                  Direitos do Usuário<br>
                  <br>
                  3.12    Em cumprimento à regulamentação aplicável, no que diz respeito ao tratamento de dados pessoais, a EMC respeita e garante ao usuário, a possibilidade de apresentação de solicitações baseadas nos seguintes direitos:<br>
                  a)    confirmação da existência de tratamento;<br>
                  b)    acesso aos dados;<br>
                  c)    correção de dados incompletos, inexatos ou desatualizados;<br>
                  d)    anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade;<br>
                  e)    portabilidade dos dados a outro fornecedor de serviço ou produto, mediante requisição expressa, de acordo com a regulamentação da autoridade nacional, observados os segredos comercial e industrial;<br>
                  f)    eliminação dos dados tratados com consentimento do usuário;<br>
                  g)    obtenção de informações sobre as entidades públicas ou privadas com as quais a EMC compartilhou seus dados;<br>
                  h)    informação sobre a possibilidade de não fornecer consentimento e sobre as consequências da negativa;<br>
                  i)    revogação do consentimento.<br>
                  3.13    O usuário poderá submeter suas solicitações de informação e questionamentos acerca deste documento, para o e-mail:  encarregado.lgpd@emc.gov.br.<br>
                  3.14    Caso a EMC modifique este documento de Diretriz de Privacidade e Proteção de Dados Pessoais, as alterações serão publicadas de forma visível no endereço www.EMC.gov.br.<br>
                  <br>
                  Este documento é válido a partir de 14/8/2020.<br>
                  <br>
                  GLOSSÁRIO<br>
                  <br>
                  1.    Consentimento: manifestação livre, informada e inequívoca pela qual o titular concorda com o tratamento de seus dados pessoais para uma finalidade determinada.<br>
                  2.    Controlador: pessoa natural ou jurídica, de direito público ou privado, a quem competem as decisões referentes ao tratamento de dados pessoais.<br>
                  3.    Dados Pessoais:  informação relacionada a pessoa natural identificada ou identificável.<br>
                  4.    Encarregado de Dados (DPO): pessoa indicada pelo controlador e operador para atuar como canal de comunicação entre o controlador, os titulares dos dados e a Autoridade Nacional de Proteção de Dados (ANPD).<br>
                  5.    Operador: pessoa natural ou jurídica, de direito público ou privado, que realiza o tratamento de dados pessoais em nome do controlador.<br>
                  6.    Titular: pessoa natural a quem se referem os dados pessoais que são objeto de tratamento.<br>
                  7.    Tratamento: toda operação realizada com dados pessoais, como as que se referem à coleta, produção, recepção, classificação, utilização, acesso, reprodução, transmissão, distribuição, processamento, arquivamento, armazenamento, eliminação, avaliação ou controle da informação, modificação, comunicação, transferência, difusão ou extração.<br>
                  8.    Usuário: Titular do dado pessoal.<br>
